function hide() {
    document.getElementById('previous-releases').style.display='none';
    document.getElementById('show').style.display='block';
    document.getElementById('hide').style.display='none';
}

function show() {
    document.getElementById('previous-releases').style.display='block';
    document.getElementById('show').style.display='none';
    document.getElementById('hide').style.display='block';
}

function hidestandalone() {
    document.getElementById('previous-releases-standalone').style.display='none';
    document.getElementById('showstandalone').style.display='block';
    document.getElementById('hidestandalone').style.display='none';
}

function showstandalone() {
    document.getElementById('previous-releases-standalone').style.display='block';
    document.getElementById('showstandalone').style.display='none';
    document.getElementById('hidestandalone').style.display='block';
}